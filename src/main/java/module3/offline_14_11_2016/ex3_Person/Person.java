package module3.offline_14_11_2016.ex3_Person;

public class Person {
    private String gender;
    private String name;
    private int age;
    private String secondName;

    public Person(String gender, String name, int age, String secondName) {
        this.gender = gender;
        this.name = name;
        this.age = age;
        this.secondName = secondName;
    }
}
