package module8.offline26122016;

public class Point {
    private int xx;
    private int yy;

    public Point(int xx, int yy) {
        this.xx = xx;
        this.yy = yy;
    }

    public int getXx() {
        return xx;
    }

    public void setXx(int xx) {
        this.xx = xx;
    }

    public int getYy() {
        return yy;
    }

    public void setYy(int yy) {
        this.yy = yy;
    }

}
